package org.episteme.natural.biology.loaders.sbml;

import org.episteme.core.mathematics.numbers.real.Real;

/**
 * Represents a global parameter in an SBML model.
 *
 * @author Silvere Martin-Michiellot
 * @author Gemini AI (Google DeepMind)
 * @since 1.0
 */
public class SBMLParameter {

    private String id;
    private String name;
    private Real value;
    private String units;
    private boolean constant = true;

    public SBMLParameter() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Real getValue() {
        return value;
    }

    public void setValue(Real value) {
        this.value = value;
    }

    public String getUnits() {
        return units;
    }

    public void setUnits(String units) {
        this.units = units;
    }

    public boolean isConstant() {
        return constant;
    }

    public void setConstant(boolean constant) {
        this.constant = constant;
    }

    @Override
    public String toString() {
        return "SBMLParameter{id='" + id + "', value=" + value + ", units='" + units + "'}";
    }
}

