package org.jscience.biology.loaders.sbml;

/**
 * Represents a gene product from the FBC extension.
 *
 * @author Silvere Martin-Michiellot
 * @author Gemini AI (Google DeepMind)
 * @since 1.0
 */
public class SBMLGeneProduct {

    private String id;
    private String name;
    private String label;
    private String metaId;

    public SBMLGeneProduct() {
    }

    public String getMetaId() { return metaId; }
    public void setMetaId(String metaId) { this.metaId = metaId; }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    @Override
    public String toString() {
        return "SBMLGeneProduct{id='" + id + "', label='" + label + "'}";
    }
}
