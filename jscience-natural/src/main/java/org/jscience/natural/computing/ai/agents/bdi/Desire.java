package org.jscience.natural.computing.ai.agents.bdi;

/**
 * Represents a Desire (Goal) in the BDI architecture.
 * <p>
 * Desires represent states of affairs that the agent would (in principle) like to achieve.
 * </p>
 *
 * @author Silvere Martin-Michiellot
 * @author Gemini AI (Google DeepMind)
 * @since 2.0
 */
public interface Desire {
    /**
     * The priority or utility of this desire.
     * @return a double representing importance (higher is more important).
     */
    double getPriority();

    /**
     * Checks if the desire has been achieved.
     * @return true if achieved.
     */
    boolean isAchieved();
}
